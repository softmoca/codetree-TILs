public class Main {
    public static void main(String[] args) {
        int a = 3;
        int b = 4;

        // 변수 a의 값을 변수 b에 넣습니다.
        b = a;

        // 변수 a와 b의 값을 곱합니다.
        int result = a * b;

        // 결과값을 출력합니다.
        System.out.printf("%d %d",a,b);
        System.out.println();
        System.out.println(result);
    }
}